palai oleh Bpk.Anton. Seiring berjalan nya waktu AJB BumiPutera sampai dengan tahun 1993-1994 dikepalai oleh Bpk.Imam Sugito pada saat itu jumlah Karyawan berjumlah kurang lebih 6 dan agen 40 orang. Di awal tahun 2000 an sampai tahun 2014 dikepalai oleh Bpk.Siswanto, berganti Bpk.Muslim kemudian Bpk.Raden Antoni Sinaga dan Bpk.Ahmad, kemudian di teruskan oleh Bpk.Cakra Buana dan pada tahun 2015 hingga sekarang dikepalai Bpk.Sidhi Abdillah S.E.
